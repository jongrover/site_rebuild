  <div id="details">
  	<p>Ants, 2008; Sound Sculpture; live ants, enclosure, sensors, electronics; 12" x 15" x 42"; Tiny sensors mounted on the sides on an ant farm trigger notes as ants pass through their digging tunnels. Each tunnel sensor responds with different pitches. The ants unknowingly compose their own melodies.</p>
