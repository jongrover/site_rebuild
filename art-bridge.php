  <div id="details">
  	<p>Bridge, 2010; Interactive sound installation; wood, anodized aluminum, custom electronics; 14" x 14" x 14", & 16" x 16" x 16"; Using wireless metal boxes as activation points, participants complete sound circuits and build up and break down resistive networks using skin to skin contact with others.</p>
